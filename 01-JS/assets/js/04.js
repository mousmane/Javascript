// Déclaration de plusieurs variables à la suite
var nb1, nb2, resultat;
nb1 = 10; nb2 = 5;

/* L'addition */
resultat = nb1 + nb2;
console.log("l'addition de " + nb1 + " et de " + nb2 + " est égale à " + resultat);

/* La soustraction */
resultat = nb1 - nb2;
console.log("la soustraction de " + nb2 + " à " + nb1 + " est égale à " + resultat);

/* La multiplication */
resultat = nb1 * nb2;
console.log("la multiplication de " + nb1 + " par " + nb2 + " est égale à " + resultat);

/* La division */
resultat = nb1 / nb2;
console.log("la division de " + nb1 + " par " + nb2 + " est égale à " + resultat);

/* Le modulo */
nb1 = 11;
resultat = nb1 % nb2;
console.log("le modulo de " + nb1 + " et " + nb2 + " est égale à " + resultat);

