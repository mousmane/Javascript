var nb1 = 1;

// Incrémentation
nb1 += 1;
/* Ecriture simplifiée */
nb1++; // ++ toujours par pas de 1

// Décrémentation
nb1 -= 1;
/* Ecriture simplifiée */
nb1--; // ++ toujours par pas de 1


nb1=0;
console.log("affichage de nb1++ : "+nb1++);  // post-décrémentation
